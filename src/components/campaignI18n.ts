export function getCampaignTranslation(lang: Language, key: string): string | undefined {
  const dict = lang === "TR" ? campaignDictionaryTR : campaignDictionaryEN;
  return dict[key];
}

export function translateCampaignContent(
  t: (key: string) => string,
  entityId: string,
  field: "subject" | "body" | "text",
  fallback: string
): string {
  const keyed = `seed.${entityId}.${field}`;
  const keyedResult = t(keyed);
  if (keyedResult !== keyed) return keyedResult;
  const fallbackResult = t(fallback);
  return fallbackResult !== fallback ? fallbackResult : fallback;
}

